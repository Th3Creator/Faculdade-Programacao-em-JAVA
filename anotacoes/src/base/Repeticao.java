package base;

public class Repeticao {
    public static void main(String[] args) {
        int a = 10;

        int contador = 0;
        while(contador < a){
            System.out.println(contador);
            contador++;
        }

        for(int i = 0; i <10 ; i++ ) {
            System.out.println(i);
        }
        // declaração de variável ; condição de parada ; contador
    }
}
